package br.com.dsystem.model;

import java.math.BigDecimal;

/**
 *
 * @author david
 */
public class Borda {
    private int id;
    String descricao;
    BigDecimal valor;

    public Borda() {
    }

    public Borda(int id, String descricao, BigDecimal valor) {
        this.id = id;
        this.descricao = descricao;
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public BigDecimal getValor() {
        return valor;
    }

    public void setValor(BigDecimal valor) {
        this.valor = valor;
    }

    @Override
    public String toString() {
        return getDescricao();
    }
    
    
    
    
}
