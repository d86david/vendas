    private void tblFuncionarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFuncionarioMouseClicked
        if (evt.getClickCount() == 2) {
            codigo = tblFuncionario.getValueAt(tblFuncionario.getSelectedRow(), 0).toString();
            dispose();
        }
    }//GEN-LAST:event_tblFuncionarioMouseClicked

    private void btnPesquisaCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaCodigoActionPerformed
        if (txtPesquisaCodigo.getText() == null || txtPesquisaCodigo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe um código para pesquisa!");
            fController.carregaTabela(tblFuncionario);
        } else {
            try {

                fController.buscarIdFuncionario(tblFuncionario, tblFuncionario.getValueAt(tblFuncionario.getSelectedRow(), 0).toString());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Funcionarios", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnPesquisaCodigoActionPerformed

    private void txtPesquisaNomeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaNomeKeyPressed
        if(txtPesquisaNome.getText().isEmpty()){
            fController.carregaTabela(tblFuncionario);
        }else{
            fController.buscarNomeFuncionario(tblFuncionario, txtPesquisaNome.getText());
        }
    }//GEN-LAST:event_txtPesquisaNomeKeyPressed

