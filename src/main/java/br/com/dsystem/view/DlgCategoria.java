    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (txtCodigo.getText().isEmpty() || txtDescricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione uma Categoria para edição!", "Editar Categoria", JOptionPane.WARNING_MESSAGE);
        } else {
            String codigoCap = txtCodigo.getText();
            try {
                habilitaEdicao();
                eNovo = false;

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Categorias", JOptionPane.WARNING_MESSAGE);
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (eNovo != true) {
            try {
                categoriaControl.atualizarCategoria(txtCodigo.getText(), txtDescricao.getText(), jrbTemBorda.isSelected(), jrbSabores.isSelected());
                categoriaControl.listarCategorias(tblCategoria);
                JOptionPane.showMessageDialog(this, "Categoria Atualizada com sucesso!", "Cadastro de Categoria", JOptionPane.INFORMATION_MESSAGE);
                fechaEdicao();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Categorias", JOptionPane.WARNING_MESSAGE);
                System.err.println(e);
            }
        } else {
            try {
                categoriaControl.cadastrarCategoria(txtDescricao.getText(), jrbTemBorda.isSelected(), jrbSabores.isSelected());
                categoriaControl.listarCategorias(tblCategoria);
                JOptionPane.showMessageDialog(this, "Categoria Cadastrada com sucesso!", "Cadastro de Categoria", JOptionPane.INFORMATION_MESSAGE);
                limpaCampo();
                fechaEdicao();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Categorias", JOptionPane.WARNING_MESSAGE);
                System.err.println(e);
            }

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if (txtCodigo.getText().isEmpty()) {
            limpaCampo();
            fechaEdicao();
            categoriaControl.listarCategorias(tblCategoria);
        } else {
            Categoria c = categoriaControl.pesquisarCodigoCategoria(txtCodigo.getText());
            txtCodigo.setText(Integer.toString(c.getId()));
            txtDescricao.setText(c.getDescricao());
            jrbTemBorda.setSelected(c.isTemBorda());
            jrbSabores.setSelected(c.isVariosSabores());
            categoriaControl.listarCategorias(tblCategoria);
            fechaEdicao();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um código para excluir!");
        } else {

            try {
                Categoria cat = categoriaControl.pesquisarCodigoCategoria(txtCodigo.getText());
                cat.setId(Integer.parseInt(txtCodigo.getText()));
                cat.setDescricao(txtDescricao.getText());
                cat.setTemBorda(jrbTemBorda.isSelected());
                cat.setVariosSabores(jrbSabores.isSelected());

                int resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão da Categoria?", "Exclusão", JOptionPane.YES_NO_OPTION);
                if (resp == JOptionPane.YES_OPTION) {
                    try {
                        categoriaControl.deletarCategoria(txtCodigo.getText());
                        categoriaControl.listarCategorias(tblCategoria);
                        JOptionPane.showMessageDialog(this, "Categoria Exluída com sucesso!", "Exclusão de Categoria", JOptionPane.WARNING_MESSAGE);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, e.getMessage(), "Categorias", JOptionPane.WARNING_MESSAGE);
                        System.err.println(e);
                    }
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Categorias", JOptionPane.WARNING_MESSAGE);
                System.err.println(e);
            }

        }


    }//GEN-LAST:event_btnExcluirActionPerformed

