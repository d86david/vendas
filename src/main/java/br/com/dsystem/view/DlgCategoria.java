    private void tblCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriaMouseClicked
        if (evt.getClickCount() == 2) {
            try {

                Categoria cat = categoriaControl.pesquisarCodigoCategoria(tblCategoria.getValueAt(tblCategoria.getSelectedRow(), 0).toString());

                txtCodigo.setText(Integer.toString(cat.getId()));
                txtDescricao.setText(cat.getDescricao());
                jrbSabores.setSelected(cat.isVariosSabores());
                jrbTemBorda.setSelected(cat.isTemBorda());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Categorias", JOptionPane.WARNING_MESSAGE);
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_tblCategoriaMouseClicked

