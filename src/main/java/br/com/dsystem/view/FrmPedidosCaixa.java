    private void tblPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPedidosMouseClicked
        PedidoCaixa p = pcController.pegaPedido(tblPedidos.getSelectedRow());

        lblStatus.setText(p.getStatusPedido());
        lblVlTotal.setText(p.getTotalPedido().toString());
        lblDtPedido.setText(p.getDataPedido());

        String ped = Integer.toString(p.getId());

        switch (ped.length()) {
            case 1 -> {
                lblNumPedido.setText("000" + ped);
            }
            case 2 -> {
                lblNumPedido.setText("00" + ped);
            }
            case 3 -> {
                lblNumPedido.setText("0" + ped);
            }
            default -> {
                lblNumPedido.setText(ped);
            }
        }
        try {
            Funcionario f = fController.buscaIdFuncionario(Integer.toString(p.getAtendenteId()));
            String nome[] = f.getNome().trim().split(" ");
            lblAtendente.setText(nome[0]);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Pedido Caixa", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tblPedidosMouseClicked

    private void btnBaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBaixaActionPerformed
        if (tblPedidos.getSelectedRow() >= 0) {
            DlgPagamento dialogPgto = new DlgPagamento(new javax.swing.JDialog(), true);
            dialogPgto.recebeBaixa(lblNumPedido.getText());
            dialogPgto.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um pedido para baixar!", "Pedido Caixa", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBaixaActionPerformed

