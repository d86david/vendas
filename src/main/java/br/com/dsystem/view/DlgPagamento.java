    private void btnFinalizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizaActionPerformed
        atuaizaPagamento();
        if (this.valorPago.compareTo(totalAPagr) < 0) {
            JOptionPane.showMessageDialog(null, "Pedido nÃ£o pode ser baixado!", "Pagamento", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Date baixa = new Date();
                String dtBaixa = sdf.format(baixa);
                pcController.baixaPedido(codigoPedido, new BigDecimal(txtTotalAPagar.getText().replace(",", ".")), new BigDecimal(txtDesconto.getText().replace(",", ".")), dtBaixa);
                 JOptionPane.showMessageDialog(null, "Pedido baixado com Sucesso!", "Pagamento", JOptionPane.INFORMATION_MESSAGE);
                 dispose();
            
            } catch (Exception e) {
                 JOptionPane.showMessageDialog(null, e.getMessage(), "Pagamento", JOptionPane.WARNING_MESSAGE);
            }
           
        }
    }//GEN-LAST:event_btnFinalizaActionPerformed

