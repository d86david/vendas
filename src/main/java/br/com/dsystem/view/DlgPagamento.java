    public void recebeBaixa( String id_pedido ){
        codigoPedido = id_pedido;
        PedidoCaixa pedido = pcController.pesquisarIdPedido(id_pedido);
        fPagto = fpController.pesquisaIdFormaPagto(Integer.toString(pedido.getFormaPagtoId()));
        lblTipoPagto.setText(fPagto.getDescricao());
        lblValorPedido.setText(pedido.getTotalPedido().toString().replace(".", ","));  
        this.vlTotalPedido = pedido.getTotalPedido();
        txtDesconto.setText("0,00");
        txtTroco.setText("0,00");
        
        if (fPagto.getDescricao().toUpperCase().equals("DINHEIRO")) {
            txtTotalAPagar.setText(this.vlTotalPedido.toString().replace(".", ","));
        } else {
            txtValorPago.setText(this.vlTotalPedido.toString().replace(".", ","));
            txtTotalAPagar.setText(this.vlTotalPedido.toString().replace(".", ","));
        }
    }

    public void recebePagto(Cliente cliente, String dataPedido, FormaPagto pagto, String status, Funcionario func, String obsPedido, String total) {
        try {
            dtPedido = sdf.parse(dataPedido);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }    
        cli = cliente;
        fPagto = pagto;
        this.status = status;
        this.func = func;
        this.obsPedido = obsPedido;

        lblTipoPagto.setText(pagto.getDescricao());
        lblValorPedido.setText(total);
        txtDesconto.setText("0,00");
        txtTroco.setText("0,00");
        this.vlTotalPedido = new BigDecimal(total.replace(",", "."));

        if (pagto.getDescricao().toUpperCase().equals("DINHEIRO")) {
            txtTotalAPagar.setText(this.vlTotalPedido.toString().replace(".", ","));
        } else {
            txtValorPago.setText(this.vlTotalPedido.toString().replace(".", ","));
            txtTotalAPagar.setText(this.vlTotalPedido.toString().replace(".", ","));
        }

    }

    public void atuaizaPagamento() {
        valorDesconto = new BigDecimal(txtDesconto.getText().replace(",", "."));
        totalAPagr = vlTotalPedido.subtract(valorDesconto);
        txtTotalAPagar.setText(totalAPagr.toString().replace(".", ","));
        valorPago = new BigDecimal(txtValorPago.getText().replace(",", "."));
        if (valorPago.compareTo(totalAPagr) >= 0) {
            troco = valorPago.subtract(totalAPagr);
            txtTroco.setText(troco.toString().replace(".", ","));
        } else {
            txtTroco.setText("0,00");
        }
    }

    private String completaCamposValores(String str) {
        if (!str.contains(",")) {
            return str + ",00";
        } else {
            return str;
        }
    }

