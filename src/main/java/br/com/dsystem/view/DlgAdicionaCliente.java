    private void btnBuscaCEPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaCEPActionPerformed
        try {
            ClienteController cliControl = new ClienteController();
            Cliente cli = cliControl.pesquisaCepCliente(txtCEP.getText());

            txtEndereco.setText(cli.getEndereco());
            txtBairro.setText(cli.getBairro());
            txtCidade.setText(cli.getCidade());
            jcbUf.setSelectedItem(cli.getEstado());

        } catch (Exception e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, e.getMessage(), "Busca de CEP", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscaCEPActionPerformed

