    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        try {
            ClienteController cliControl = new ClienteController();

            cliControl.cadastrarCliente(txtNome.getText(), txtCelular.getText(), "", (String) jcbPessoa.getSelectedItem(), txtCpfCnpj.getText(), "", txtCEP.getText(), txtEndereco.getText(), txtNumero.getText(),
                    txtCompl.getText(), txtBairro.getText(), txtCidade.getText(), (String) jcbUf.getSelectedItem(), "", txtObs.getText());
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Cadastro de Cliente", JOptionPane.ERROR_MESSAGE);
            System.err.println(e.getMessage());
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void jcbPessoaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbPessoaItemStateChanged
        // Alternando a Mascara de Documento conforme Tipo de pessoa
        if (jcbPessoa.getSelectedIndex() == 0) {
            //Máscara para Pessoa Física
            txtCpfCnpj.setValue(null); // Limpando o campo
            try { //try-catch proteje o código de possiveis erros de conersão de Mascara
                txtCpfCnpj.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("###.###.###-##")));
            } catch (ParseException e) {
            }
        } else if (jcbPessoa.getSelectedIndex() == 1) {
            //Máscara para pessoa Jurídica
            txtCpfCnpj.setValue(null); // Limpando o campo
            try { //try-catch proteje o código de possiveis erros de conersão de Mascara
                txtCpfCnpj.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("##.###.###/####-##")));
            } catch (ParseException e) {
            }
        }
    }//GEN-LAST:event_jcbPessoaItemStateChanged

