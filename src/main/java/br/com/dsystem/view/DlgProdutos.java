    private void limpaCampos() {
        txtCPersonalizado.setText("");
        txtDescricao.setText("");
        txtValor.setText("");
        txtEstoque.setText("");
        jrbCEsqoque.setSelected(false);
    }
    
    private void habilitaEdicao() {
        txtCPersonalizado.setEditable(true);
        txtDescricao.setEditable(true);
        jcbCategoria.setEnabled(true);
        txtValor.setEditable(true);
        txtEstoque.setEditable(true);
        jrbCEsqoque.setEnabled(true);
        btnNovo.setVisible(false);
        btnEditar.setVisible(false);
        btnExcluir.setVisible(false);
        btnSalvar.setVisible(true);
        btnCancelar.setVisible(true);
        
    }
    
    private void fechaEdicao() {
        txtCPersonalizado.setEditable(false);
        txtDescricao.setEditable(false);
        jcbCategoria.setEnabled(false);
        txtValor.setEditable(false);
        txtEstoque.setEditable(false);
        jrbCEsqoque.setEnabled(false);
        btnNovo.setVisible(true);
        btnEditar.setVisible(true);
        btnExcluir.setVisible(true);
        btnSalvar.setVisible(false);
        btnCancelar.setVisible(false);
    }
    
    private String completaCampos(String str){
        switch (str.length()) {
            case 1 -> {
                return "000" + str;
            }
            case 2 -> {
                return "00" + str;
            }
            case 3 -> {
                return "0" + str;
            }
            default -> {
                return str;
            }
        }
    }
    
