    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Produto prod = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        prod.setCodigoProduto(Integer.parseInt(txtCPersonalizado.getText()));
        prod.setDescricao(txtDescricao.getText());
        prod.setPreco(new BigDecimal(txtValor.getText().replace(",", ".")));
        prod.setQtdEstoquue(Integer.parseInt(txtEstoque.getText()));
        prod.setCategoria((Categoria) jcbCategoria.getSelectedItem());
        prod.setControlaEstoque(jrbCEsqoque.isSelected());
        
        if (txtCodigo.getText().isEmpty()) {
            if (dao.temProduto(txtDescricao.getText()) == true || dao.temProduto(Integer.parseInt(txtCPersonalizado.getText()))) {
                JOptionPane.showMessageDialog(null, "Produto já cadastrado");
            } else {
                dao.cadastraProduto(prod);
                limpaCampos();
                fechaEdicao();
            }
        } else {
            prod.setId(Integer.parseInt(txtCodigo.getText()));
            dao.editaProduto(prod);
            fechaEdicao();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisaCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaCodigoActionPerformed
        if (txtPesquisaProduto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um produto");
        } else {
            ProdutoDAO dao = new ProdutoDAO();
            String descricao = "%" + txtPesquisaProduto.getText() + "%";
            
            List<Produto> produtos = dao.pesquisaProduto(descricao);
            
            DefaultTableModel tblProdutos = (DefaultTableModel) tblProduto.getModel();
            tblProdutos.setNumRows(0);
            
            for (Produto p : produtos) {
                tblProdutos.addRow(new Object[]{
                    p.getId(),
                    p.getDescricao(),
                    p.getPreco(),
                    p.getCategoria().getDescricao()
                });
                
            }
        }
    }//GEN-LAST:event_btnPesquisaCodigoActionPerformed

