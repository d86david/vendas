    private void lblBotaoMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBotaoMenuMouseClicked
        clicaMenu(jpEsconeMenu, jpConfig, 1);
        // criar vazio para o método ocultar e mostrar o menu do painel
        if(a==true){
            mostraEsconde(jpMenu, a, lblBotaoMenu);
            SwingUtilities.updateComponentTreeUI(this);
            //Criar método que muda a imagem do menu
            a=false;
        }else{
            mostraEsconde(jpMenu, a, lblBotaoMenu);
            SwingUtilities.updateComponentTreeUI(this);
            a=true;
        }
        
        
    }//GEN-LAST:event_lblBotaoMenuMouseClicked

