    /* 
    *Método que muda a cor da linha acima do menu no painel lateral, inicadno a seleção da opção 
    */
    public void trocaCor(JPanel hover, Color rand){
        hover.setBackground(rand);
    }
    
    /**
    *  Método que altera a cor do botão ao clicar 
     * @param h1
     * @param h2
     * @param numberbool
    */
    public void clicaMenu( JPanel h1, JPanel h2, int numberbool){
        if(numberbool == 1){
            h1.setBackground(new Color(25,29,74));
            h2.setBackground(new Color(5,10,46));
        }else{
            h1.setBackground(new Color(5,10,46));
            h2.setBackground(new Color(25,29,74));
        }
        
    }
    
    /**
    *  Método que altera a imagem do menu lateral
     * @param botao 
     * @param resourceImg
    */
    
    public void mudaImagem(JLabel botao,String resourceImg ){
        ImageIcon aimg = new ImageIcon(getClass().getResource(resourceImg));
        botao.setIcon(aimg);
    }
    
    /**
     *  Método que oculta e mostrar o menu do painel
     * @param menushow
     * @param dashboard
     * @param botao
     */
    public void mostraEsconde(JPanel menushow, boolean dashboard, JLabel botao){
    if(dashboard == true){
        menushow.setPreferredSize(new Dimension(50, menushow.getHeight()));
        mudaImagem(botao, "/images/menu_32px.png");
    }else{
        menushow.setPreferredSize(new Dimension(270, menushow.getHeight()));
        mudaImagem(botao, "/images/back_32px.png");
    }
        
    }
    
