    private void btnBuscarCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCodigoActionPerformed

        if (txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um código para pesquisa!", "Busca de Cliente", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                Cliente cli = clienteControl.pesquisaCodigoCliente(txtCodigo.getText());

                txtCodigo.setText(Integer.toString(cli.getId()));
                txtNome.setText(cli.getNome());
                txtCelular.setText(cli.getCelular());
                txtFixo.setText(cli.getFoneFixo());
                jcbPessoa.setSelectedItem(cli.getPessoa());
                txtCpfCnpj.setText(cli.getCpfCnpj());
                txtRG.setText(cli.getRg());
                txtCEP.setText(cli.getCep());
                txtEndereco.setText(cli.getEndereco());
                txtNumero.setText(Integer.toString(cli.getNumero()));
                txtCompl.setText(cli.getCompl());
                txtBairro.setText(cli.getBairro());
                txtCidade.setText(cli.getCidade());
                jcbEstado.setSelectedItem(cli.getEstado());
                txtEmail.setText(cli.getEmail());
                txtObs.setText(cli.getObs());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Clientes", JOptionPane.WARNING_MESSAGE);
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btnBuscarCodigoActionPerformed

