    private void jcbPessoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbPessoaActionPerformed
        // Alternando a Mascara de Documento conforme Tipo de pessoa
        if (jcbPessoa.getSelectedIndex() == 0) {
            //Máscara para Pessoa Física
            txtCpfCnpj.setValue(null); // Limpando o campo
            try { //try-catch proteje o código de possiveis erros de conersão de Mascara
                txtCpfCnpj.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("###.###.###-##")));
            } catch (ParseException e) {
            }
        } else if (jcbPessoa.getSelectedIndex() == 1) {
            //Máscara para pessoa Jurídica
            txtCpfCnpj.setValue(null); // Limpando o campo
            try { //try-catch proteje o código de possiveis erros de conersão de Mascara
                txtCpfCnpj.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("##.###.###/####-##")));
            } catch (ParseException e) {
            }
        }
    }//GEN-LAST:event_jcbPessoaActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (txtCodigo.getText().isEmpty() || txtNome.getText().isEmpty() || txtCelular.getText().isEmpty()) {
                clienteControl.cadastrarCliente(txtNome.getText(), txtCelular.getText(),
                        txtFixo.getText(), jcbPessoa.getSelectedItem().toString(), txtCpfCnpj.getText(), txtRG.getText(), txtCEP.getText(),
                        txtEndereco.getText(), txtNumero.getText(), txtCompl.getText(), txtBairro.getText(), txtCidade.getText(),
                        jcbEstado.getSelectedItem().toString(), txtEmail.getText(), txtObs.getText());

                JOptionPane.showMessageDialog(null, "Cliente Cadastrado com sucesso", "Clientes", JOptionPane.INFORMATION_MESSAGE);
                limpaCampos();
                fechaEdicao();

            } else {
                clienteControl.atuaizarCliente(txtCodigo.getText(), txtNome.getText(), txtCelular.getText(),
                        txtFixo.getText(), jcbPessoa.getSelectedItem().toString(), txtCpfCnpj.getText(), txtRG.getText(), txtCEP.getText(),
                        txtEndereco.getText(), txtNumero.getText(), txtCompl.getText(), txtBairro.getText(), txtCidade.getText(),
                        jcbEstado.getSelectedItem().toString(), txtEmail.getText(), txtObs.getText());

                JOptionPane.showMessageDialog(null, "Cliente Editado com sucesso", "Cadastro de Cliente", JOptionPane.PLAIN_MESSAGE);
                limpaCampos();
                fechaEdicao();
            }

        } catch (ClienteExceptions e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

